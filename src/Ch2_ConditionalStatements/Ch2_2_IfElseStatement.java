package Ch2_ConditionalStatements;

public class Ch2_2_IfElseStatement {
    public static void main(String[] args) {
        int a = 8;
        // if bloğu içerisine yazılan statement true dönerse if bloğu içerisindeki kodlar çalışır,
        //false dönerse else bloğu içerisindeki kodlar çalışır.
        if (a>10){
            System.out.println("number is greater than 10");
        } else{
            System.out.println("number is smaller than 10");
        }
    }
}
